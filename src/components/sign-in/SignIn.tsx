export default function SignIn(props: { disableCustomTheme?: boolean }) {
  var usuario = new Usuario()


  const [emailError, setEmailError] = React.useState(false);
  const [emailErrorMessage, setEmailErrorMessage] = React.useState('');
  const [passwordError, setPasswordError] = React.useState(false);
  const [passwordErrorMessage, setPasswordErrorMessage] = React.useState('');
  const [open, setOpen] = React.useState(false);

 

  const handleClose = () => {
    setOpen(false);
  };

  const handleSubmit = (event: React.FormEvent<HTMLFormElement>) => {
    if (emailError || passwordError) {
      event.preventDefault();
      return;
    }
    const data = new FormData(event.currentTarget);
    console.log({
      email: data.get('email'),
      password: data.get('password'),
    });
  };

  const validateInputs = () => {
    const email = document.getElementById('email') as HTMLInputElement;
    const password = document.getElementById('password') as HTMLInputElement;

    let isValid = true;

    if (!email.value || email.value !=='sandbox@factus.com.co') {
      setEmailError(true);
      setEmailErrorMessage('Usuario incorrecto');
      isValid = false;
    } else {
      setEmailError(false);
      setEmailErrorMessage('');
    }

    if (!password.value || password.value !=='sandbox2024%') {
      setPasswordError(true);
      setPasswordErrorMessage('Contraseña incorrecta');
      isValid = false;
    } else {
      setPasswordError(false);
      setPasswordErrorMessage('');
    }

    usuario.username=email.value
    usuario.password=password.value
    console.log(usuario)
   
    singInServi(usuario).then(data=>{
      Cookies.remove('authToken');
      saveToken(data.access_token)
    }).catch(error =>{
      console.log(error)
    })
    

  };

  return (
    <AppTheme {...props}>
      <CssBaseline enableColorScheme />
      <SignInContainer direction="column" justifyContent="space-between">
        <ColorModeSelect sx={{ position: 'fixed', top: '1rem', right: '1rem' }} />
        <Card variant="outlined">
          <Typography
            component="p"
            sx={{ margin: '0' }}>

            <div style={{ display: 'inline-flex', alignItems: 'center' }}>
            <Icon icon="simple-icons:lionair" style={{ marginRight: '3px', marginLeft:'3px', fontSize: '24px' }} />
              <span>Gtrab618</span>
              <Icon icon="eos-icons:atom-electron" style={{ marginRight: '3px', marginLeft:'3px', fontSize: '24px' }} />
              <span>Halltec</span>
              <Icon icon="material-symbols:format-text-clip-rounded" />
            </div>
            
          </Typography>


          <Typography
            component="h1"
            variant="h4"
            sx={{ width: '100%', fontSize: 'clamp(2rem, 10vw, 2.15rem)' }}
          >
            Login
          </Typography>
          <Box
            component="form"
            onSubmit={handleSubmit}
            noValidate
            sx={{
              display: 'flex',
              flexDirection: 'column',
              width: '100%',
              gap: 2,
            }}
          >
            <FormControl>
              <FormLabel htmlFor="email">Email</FormLabel>
              <TextField
                error={emailError}
                helperText={emailErrorMessage}
                id="email"
                type="email"
                name="email"
                defaultValue={"sandbox@factus.com.co"}
                autoComplete="email"
                autoFocus
                required
                fullWidth
                variant="outlined"
                color={emailError ? 'error' : 'primary'}
              />
            </FormControl>
            <FormControl>
              <FormLabel htmlFor="password">Password</FormLabel>
              <TextField
                error={passwordError}
                helperText={passwordErrorMessage}
                name="password"
                defaultValue={"sandbox2024%"}
                placeholder="••••••"
                id="password"
                autoComplete="current-password"
                autoFocus
                required
                fullWidth
                variant="outlined"
                color={passwordError ? 'error' : 'primary'}
              />
            </FormControl>
            <FormControlLabel
              control={<Checkbox value="remember" color="primary" />}
              label="Remember me"
            />
            <ForgotPassword open={open} handleClose={handleClose} />
            <Button
              
              fullWidth
              variant="contained"
              onClick={validateInputs}
            >
              Sign in
            </Button>
     
          </Box>
    
          <Box sx={{ display: 'flex', flexDirection: 'column', gap: 2 }}>

          </Box>
        </Card>
      </SignInContainer>
    </AppTheme>
  );
}
